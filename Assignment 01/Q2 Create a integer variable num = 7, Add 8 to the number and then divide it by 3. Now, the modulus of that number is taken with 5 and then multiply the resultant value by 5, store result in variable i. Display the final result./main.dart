void main(List<String> args) {
  int num = 7;
  print("The Given number is: $num");
  num += 8;
  print("The Addition of 7 + 8 is: $num");
  double divide = num / 3;
  print("The Division of $num by 3 is: $divide");
  double mod = divide % 5;
  print("The Modulus of $divide by 5 is: $mod");
  double i = mod * 5;
  print("The Multiplication of $mod by 5 is: $i");
  print("The Final Result is: $i");
}
